public class ExampleTwo{
    public static void main(String[] args){
        String str = "Welcome to string handling tutorial";
        
        char ch2 = str.charAt(7);
        
        char ch3 = str.charAt(11);
        
        char ch4 = str.charAt(20);
        
        System.out.println(str.charAt(0));
        System.out.println("character at 7 index is: "+ch2);
        System.out.println("character at 11 index is: "+ch3);
        System.out.println("character at 20 index is: "+ch4);
    }
}