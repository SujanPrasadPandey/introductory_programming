public class AreaCircumferenceOfCircle{
    public static void main(String[] args){
        double pie=3.1415;
        int r=10;
        double c= 4.0*pie*r;
        double a=pie*r*r;
        System.out.println ("the circumference of circle is: "+c);
        System.out.println("the area of circle is: "+a);
    }
}